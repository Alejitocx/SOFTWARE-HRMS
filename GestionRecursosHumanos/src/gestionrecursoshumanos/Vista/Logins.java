public void validar() {
        String correo = jTextcoreeo1.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        if (!"".equals(correo) && !"".equals(pass)) {
            if (correo.equals(userEmail) && pass.equals(userPass)) {
                // Usuario normal
                SwingUtilities.invokeLater(() -> {
                    // Crear el JFrame
                    JFrame frame = new JFrame("GESTION DE RECURSOS HUMANOS - Usuario");
                    frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    frame.setSize(1400, 804);

                    // Crear el panel Usuario y a単adirlo al frame
                    Usuario panel = new Usuario();
                    frame.add(panel);

                    // Hacer visible el frame
                    frame.setVisible(true);
                });
            } else if (correo.equals(adminEmail) && pass.equals(adminPass)) {
                // Administrador
                SwingUtilities.invokeLater(() -> {
                    // Crear el JFrame
                    JFrame frame = new JFrame("GESTION DE RECURSOS HUMANOS - Administrador");
                    frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    frame.setSize(1400, 804);

                    // Crear el panel Administrador y a単adirlo al frame
                    Administradors panel = new Administradors();
                    frame.add(panel);

                    // Hacer visible el frame
                    frame.setVisible(true);
                });
            } else {
                JOptionPane.showMessageDialog(null, "Correo o Contrase単a incorrecta");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el correo y la contrase単a");
        }
    }


